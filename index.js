// define the function expression divide
// divides two numbers
const divide = function(num1, num2){
    return num1 / num2;
}

// define the arrow function square
// takes one parameter and multiplies it times itself
const square = (num) => num * num;

// define the arrow function add
// takes two parameters and adds them together
const add = (num1, num2) => num1 + num2;